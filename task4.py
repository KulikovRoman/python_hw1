# Задача 8: Требуется определить, можно ли от шоколадки размером n × m долек отломить k долек, если 
# разрешается сделать один разлом по прямой между дольками (то есть разломить шоколадку на два прямоугольника).

# за один раз можно отломить количество долек кратное либо n, либо m. Также число долек для разлома 
# должно быть меньше, чем количество кубиков шоколадки.

n = int(input("Введите колиетсво долек шоколодки погоризонтали "))
m = int(input("Введите колиетсво долек шоколодки повертикали "))
k = int(input("Введите колиетсво долек для разлома "))

max_pieces = n * m
if k >= max_pieces:
    print("Введите k еще раз")
elif k % n == 0 or k % m == 0:
    print(f"От шоколодки можно отлимить {k} дольки за одно действие")
else:
   print(f"От шоколодки нельзя отлимить {k} дольки за одно действие")